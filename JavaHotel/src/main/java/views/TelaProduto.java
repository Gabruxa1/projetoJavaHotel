    private void tblCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCadastradosMouseClicked
        // TODO add your handling code here:
        ItemEstoqueDAO dao = new ItemEstoqueDAO();
        ItemEstoque i = null;
        i = dao.find(Integer.parseInt(tblCadastrados.getValueAt(tblCadastrados.getSelectedRow(), 0).toString()));

        txtDescricao.setText(i.getNome());
        txtValorUnitario.setText(String.valueOf(i.getPrecoUnitario()));
        txtQuantidade.setText(String.valueOf(i.getPrecoUnitario()));

        for (int k = 0; k < cmbTipoProduto.getItemCount(); k++) {
            if (cmbTipoProduto.getItemAt(k).getId() == i.getTipoProduto().getId()) {
                cmbTipoProduto.setSelectedIndex(k);
            }
        }
    }//GEN-LAST:event_tblCadastradosMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        ItemEstoqueDAO dao = new ItemEstoqueDAO();

        if (tblCadastrados.getSelectedRow() != 1) {
            dao.update(cadProduto());
            limpar();
            preencherTabela();
        }
    }//GEN-LAST:event_btnEditarActionPerformed


