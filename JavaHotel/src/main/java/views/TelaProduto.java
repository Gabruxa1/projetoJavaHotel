    public void limpar() {
        txtDescricao.setText("");
        txtQuantidade.setText("");
        txtValorUnitario.setText("");
        cmbTipoProduto.setSelectedIndex(0);
    }

    public void preencherTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tblCadastrados.getModel();
        dtm.setRowCount(0);
        ItemEstoqueDAO dao = new ItemEstoqueDAO();

        for (ItemEstoque i : dao.read()) {
            dtm.addRow(new Object[]{
                i.getId(),
                i.getNome(),
                i.getPrecoUnitario(),
                i.getQtdEstoque(),
                i.getTipoProduto()
            });
        }
    }

    public void preencherCmbTipoProduto() {
        TipoProdutoDAO dao = new TipoProdutoDAO();

        for (TipoProduto tp : dao.read()) {
            cmbTipoProduto.addItem(tp);
        }
    }

    public ItemEstoque cadProduto() {
        ItemEstoque i = new ItemEstoque();

        i.setNome(txtDescricao.getText());
        i.setPrecoUnitario(Double.parseDouble(txtValorUnitario.getText()));
        i.setQtdEstoque(Integer.parseInt(txtQuantidade.getText()));
        i.setTipoProduto((TipoProduto) cmbTipoProduto.getSelectedItem());

        if (tblCadastrados.getSelectedRow() != -1) {
            i.setId(Integer.parseInt(tblCadastrados.getValueAt(tblCadastrados.getSelectedRow(), 0).toString()));
        }

        return i;
    }

